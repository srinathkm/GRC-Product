/**
 * Simple i18n: translations and number formatting for English and Arabic.
 * Use t(lang, key) for strings and formatNumber(lang, n) for numeric display.
 */

const ARABIC_NUMERALS = '٠١٢٣٤٥٦٧٨٩';

function toArabicNumerals(n) {
  if (n == null || Number.isNaN(Number(n))) return String(n);
  return String(n).replace(/[0-9]/g, (d) => ARABIC_NUMERALS[Number(d)]);
}

export function formatNumber(lang, n) {
  if (lang === 'ar') return toArabicNumerals(n);
  return n != null && !Number.isNaN(Number(n)) ? String(n) : String(n);
}

